"""
Feature flags for toggling design system components in Pixoo Commander.
These constants allow enabling/disabling parts of the visual uplift without code changes.
"""

# Design System Flags
DESIGN_SYSTEM_ENABLED: bool = True  # Enables token-based styling and components
ASSETS_ENABLED: bool = True  # Enables asset management and icon handling
MOTION_ENABLED: bool = True  # Enables animations and transitions
RENDERING_V2_ENABLED: bool = True  # Enables new rendering pipeline for previews